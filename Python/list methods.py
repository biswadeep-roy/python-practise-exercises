# sorting lists

l1 = [1,6,7,2,21,15]
l1.sort()
print(l1)

l1.reverse() # reverses the list
print(l1)

l1.append(45) # adds at the end of the list
l1.append(85)
print(l1)

# insert
l1.insert(0,544)
print(l1)
l1.insert(2,544) # inserts 522 at index number 2
print(l1)

l1.pop(2) # removes item from index value 2
print(l1)

l1.remove(21) # removes 21 from the list
print(l1)

